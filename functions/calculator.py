def add(x,y):
    result= x+y
    return result

def subtract(x,y):
    subresults=x-y
    return subresults

def devide(x,y):
    divresults=x/y
    return divresults

def multiply(x,y):
    multresults=x*y
    return multresults

def powerof(x,y):
    powerresults=x**y
    return powerresults

def sum(*numbers):
    total = 0
    for number in numbers:
        total+=number
    return total

def multiply_many(*numbers):
    mul = 0
    for number in numbers:
        mult*=number
    return mul


